package com.alos.cursojava.aula1;

public class FloatingVariable {

	public static void main(String[] args) {
		double passagePrice = 2.90;		
		float tomatoPrice = 3.95f;
		
		System.out.println("Valor da Passagem = " + passagePrice);
		System.out.println("Valor da Passagem = " + tomatoPrice);

	}

}
