package com.alos.cursojava.aula3;

public class AritmeticOperators {

	public static void main(String[] args) {
		
		int result = 1 + 2;
		System.out.println(result);
		
		result = result -1;
		System.out.println(result);
		
		result = result * 2;
		System.out.println(result);
		
		result = result / 2;
		System.out.println(result);
		
		result = result + 8;
		System.out.println(result);
		
		result = result % 7;
		System.out.println(result);
		
		result++;
		System.out.println(result);		
		
		System.out.println(result++);
		System.out.println(result);
		System.out.println(++result);
		
		System.out.println(result--);
		System.out.println(result);
		System.out.println(--result);
	}

}
