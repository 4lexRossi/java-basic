package com.alos.cursojava.aula3;

public class RelationalOperators {
	public static void main(String[] args) {
	 int value1 = 1;
	 int value2 = 2;
	 
	 System.out.println("Valor 1 == Valor 2: " + (value1 == value2));
	 System.out.println("Valor 1 != Valor 2: " + (value1 != value2));
	 System.out.println("Valor 1 > Valor 2: " + (value1 > value2));
	 System.out.println("Valor 1 >= Valor 2: " + (value1 >= value2));
	 System.out.println("Valor 1 < Valor 2: " + (value1 < value2));
	 System.out.println("Valor 1 <= Valor 2: " + (value1 <= value2));
	}
}
